const month = 1
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("may");
        break;

    default:
        console.log("default case");
        break;
}

